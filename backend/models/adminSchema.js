import Mongoose from "mongoose";
const AdminSchema = new Mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Admin = Mongoose.model("Admin", AdminSchema);
export default Admin;
